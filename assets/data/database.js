// 補齊其他發現欄位：NRBC / Giant PLT / Megakaryocyte / Smudge cell / Artefact
// 若資料庫原本未提供，統一預設為 '-'，避免前端介面取值出現 undefined
(function ensureOtherFindingsFields() {
  if (!APP_DATABASE || !Array.isArray(APP_DATABASE.specimens)) return;
  var keys = ['nrbc', 'giantPlt', 'megakaryocyte', 'smudgeCell', 'artefact'];
  APP_DATABASE.specimens.forEach(function (s) {
    if (!s.metrics) s.metrics = {};
    if (!s.prevReport) s.prevReport = {};
    keys.forEach(function (k) {
      if (typeof s.metrics[k] === 'undefined') s.metrics[k] = '-';
      if (typeof s.prevReport[k] === 'undefined') s.prevReport[k] = '-';
    });
  });
})();

// 生成「流式細胞計數」資料：
// - 結構：每筆檢體新增 s.flowCyt 物件
// - 內容：常見成熟細胞 (Band / Seg / Eo / Mono / Baso / Lym) 以 metrics 的值為基準
// - 未成熟與異常細胞 (Atypical Lym / Blast / Promyelocyte / Myelocyte / Metamyelocyte / Hypersegmented / Promonocyte / Plasma cell / Abnormal Lym)
//   在流式儀無法提供，統一設為 '-'。
(function ensureFlowCytData() {
  if (!APP_DATABASE || !Array.isArray(APP_DATABASE.specimens)) return;
  APP_DATABASE.specimens.forEach(function (s) {
    if (!s.metrics) s.metrics = {};
    if (!s.flowCyt) s.flowCyt = {};
    var m = s.metrics;
    var f = s.flowCyt;

    var copyKeys = ['band', 'segmentedNeutrophil', 'eosinophil', 'monocyte', 'basophil', 'lymphocyte'];
    copyKeys.forEach(function (k) {
      if (typeof f[k] === 'undefined') {
        f[k] = typeof m[k] !== 'undefined' ? m[k] : '-';
      }
    });

    var emptyKeys = ['atypicalLymphocyte', 'blast', 'promyelocyte', 'myelocyte', 'metamyelocyte', 'hypersegmented', 'promonocyte', 'plasmaCell', 'abnormalLymphocyte'];
    emptyKeys.forEach(function (k) {
      f[k] = '-';
    });
  });
})();

// 生成 CBC 面板數據：WBC, RBC, Hb, HCT, MCV, MCH, MCHC, PLT
// - WBC / PLT：直接沿用 metrics.wbc / metrics.plt，確保與 PLT Check 規則一致
// - 其他欄位：若尚未定義，給一組合理虛擬值
(function ensureCbcData() {
  if (!APP_DATABASE || !Array.isArray(APP_DATABASE.specimens)) return;
  APP_DATABASE.specimens.forEach(function (s) {
    if (!s.metrics) s.metrics = {};
    if (!s.cbc) s.cbc = {};
    var m = s.metrics;
    var c = s.cbc;

    if (typeof c.wbc === 'undefined') c.wbc = m.wbc || '-';
    if (typeof c.plt === 'undefined') c.plt = m.plt || '-';

    if (typeof c.rbc === 'undefined') c.rbc = '4.20';
    if (typeof c.hb === 'undefined') c.hb = '13.5';
    if (typeof c.hct === 'undefined') c.hct = '40.0';
    if (typeof c.mcv === 'undefined') c.mcv = '90.0';
    if (typeof c.mch === 'undefined') c.mch = '30.0';
    if (typeof c.mchc === 'undefined') c.mchc = '33.0';
  });
})();


